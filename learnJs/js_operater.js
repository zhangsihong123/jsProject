//JavaScript运算符

/**
 * 算术运算符
 * 运算符	描述	            例子	结果
 * +	    加	                x=y+2	x=7
 * -	    减	                x=y-2	x=3
 * *	    乘	                x=y*2	x=10
 * /	    除	                x=y/2	x=2.5
 * %	    求余数 (保留整数)	 x=y%2   x=1
 * ++	    累加	            x=++y	x=6
 * --	    递减	            x=--y	x=4
 */

/**
 * 赋值运算符
 * 
 * 运算符	例子	等价于	    结果
 *   =	    x=y	 	...         x=5
 *   +=	    x+=y	x=x+y	    x=15
 *   -=	    x-=y	x=x-y	    x=5
 *   *=	    x*=y	x=x*y	    x=50
 *   /=	    x/=y	x=x/y	    x=2
 *   %=	    x%=y	x=x%y	    x=0
 */

